struct hash_tuple {
  size_t operator()(const std::tuple<TQEType, unsigned, unsigned>& t) const {
    return static_cast<unsigned>(std::get<0>(t)) * 10000 +
           (std::get<1>(t) + 1) * 100 + std::get<2>(t);
  }
};

struct hash_pair {
  size_t operator()(const std::pair<unsigned, unsigned>& pair) const {
    return pair.first * 100 + pair.second;
  }
};

